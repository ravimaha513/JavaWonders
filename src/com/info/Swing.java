package com.info;

public class Swing {

    String swing;
    String rope;

    public Swing(String swing, String rope) {
        this.swing = swing;
        this.rope = rope;
    }

    public String getSwing() {
        return swing;
    }

    public void setSwing(String swing) {
        this.swing = swing;
    }

    public String getRope() {
        return rope;
    }

    public void setRope(String rope) {
        this.rope = rope;
    }
}
